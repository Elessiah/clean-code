package ex1;

public class Calcul {

    /**
     * Calcul l'addition de a + b
     * @param a int à additionner
     * @param b int à additionner
     * @return la somme des deux paramètres
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * Calcul la soustraction de a - b
     * @param a valeur à soustraire
     * @param b valeur qui va être soustraite à a
     * @return
     */
    public int substraction(int a, int b) {
        return a - b;
    }
}
